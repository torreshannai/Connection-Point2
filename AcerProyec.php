            <h1 style="text-align:center">CONNECTION POINT </h1>
            <h2 style="text-align:center">INFRAESTRUCTURA DE SERVICIOS DE RED </h1>
            <p><h3 style="text-align:justify">La infraestructura de red en cualquier organización es crucial para garantizar una comunicación eficiente y segura entre los diferentes sistemas y dispositivos que utilizan tanto los médicos como los clientes. 
                <br/>
                <br/>
                Una red informática es un sistema interconectado de dispositivos electrónicos, como computadoras, servidores, dispositivos móviles y otros dispositivos, que se comunican entre sí para compartir recursos, transmitir datos y permitir la colaboración en línea. 
                <br/>
                A esta red también se le llama red de computadoras o red de ordenadores y pueden ser tanto redes locales que conectan dispositivos dentro de un área limitada, como redes globales, tal es el caso del Internet, que abarcan todo el mundo.

                </h2></p>
            
        <h3>Objetivo general:
        <br>Diseñar una infraestructura de red para mejorar la comunicación y el intercambio de datos dentro de una organización, cumpliendo con los estándares y normativas establecidas en México.
        </h3>

        <h3>Objetivos especificos:
        </br>
            •	Proteger los datos de la organización.
        </br>
            •	Brindar seguridad a la organización.
            </br>
            •	Solucionar las necesidades dentro de una organización.
            </h3>

<h3>Meta: Mejorar los servicios de red y satisfacer las necesidades fundamentales de la infraestructura de la organización.</h3>

